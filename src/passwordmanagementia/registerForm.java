    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        db j = new db();
        String userR = txtUserRegister.getText();
        String emailR = txtEmailRegister.getText();
        String passR1 = String.valueOf(txtPass1Register.getPassword());
        Random rnd = new Random();
        int random = 100000 + rnd.nextInt(900000);
        String secretKey = String.valueOf(random);
        String secretKeyUser = secretKey + userR;
        passR1 = aes.encrypt(passR1, secretKeyUser);//encrypt pass based on concated string
        
        if(verifyFields()){ //checks whether all fields are filled in and that pass1 and pass2 match
            if(j.uniqueUsername(userR)){ //checks database as to whether username is unique
                j.createAccount(userR, emailR, passR1, secretKey); //passes values into database
            }
        }
    }//GEN-LAST:event_jButtonRegisterActionPerformed

