    public boolean verifyFields(){
        String userR = txtUserRegister.getText();
        String emailR = txtEmailRegister.getText();
        String passR1 = String.valueOf(txtPass1Register.getPassword());
        String passR2 = String.valueOf(txtPass2Register.getPassword());
        
        if(userR.trim().equals("") || emailR.trim().equals("") || passR1.trim().equals("") || passR2.trim().equals("")
                || userR.trim().equals("username") || emailR.trim().equals("email") || passR1.trim().equals("password") || passR2.trim().equals("password")){
            //ensures all fields are full
            JOptionPane.showMessageDialog(null, "One Or More Fields Are Empty");
            return false;
        }
        else if(!passR1.equals(passR2)){
            JOptionPane.showMessageDialog(null, "Passwords Don't Match", "Confirm Password",2);
            return false;
        }else{
            return true;
        }
    }
    
